let calculator = {
    plus: function (num1, num2) {
        return num1 + num2
    },
    minus: function (num1, num2) {
        return num1 - num2
    },
    multiplied: function (num1, num2) {
        return num1 * num2
    },
    divided: function (num1, num2) {
        return num1 / num2
    }
};
export default calculator